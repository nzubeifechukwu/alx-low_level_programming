/**
 * op_add - sums two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: sum of the two integers
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts an number from another
 * @a: first integer
 * @b: second integer
 *
 * Return: difference of the two integers
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: product of the two integers
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides an integer by another
 * @a: dividend
 * @b: divisor
 *
 * Return: quotient after division
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - divides an integer by another
 * @a: dividend
 * @b: divisor
 *
 * Return: remainder after division
 */
int op_mod(int a, int b)
{
	return (a % b);
}
